<?php

/**
 * Arithmetic Operators
 * Addition +
 * Subtraction - 
 * Multiplication *
 * Division /
 * Modulus %
 * Exponentiation 
 */
$x = 2;
$y = 7;

// Addition +
print_r($x + $y); // 9

// Subtraction -
print_r($y - $x); // 5

// Multiplication *
print_r($x * $y); // 14

// Division /
print_r($y / $x); // 3.5 

// Modulus %
print_r($y % $x); // 1

// Exponentiation
print_r($x ** $y); // 128

